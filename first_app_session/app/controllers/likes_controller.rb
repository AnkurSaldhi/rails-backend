class LikesController < ApplicationController
  before_action :authenticate_user!
  before_action :set_like, only: [:show, :edit, :update, :destroy]

  # GET /likes
  # GET /likes.json
  def index
    @likes = Like.all
  end

  # GET /likes/1
  # GET /likes/1.json
  def show
  end

  # GET /likes/new
  def new
    #debugger
    @like = Like.new
  end

  # GET /likes/1/edit
  def edit
  end

  # POST /likes
  # POST /likes.json
  def create
    #byebug
    @like = Like.new(:micropost_id => params[:like][:micropost], :user_id => current_user.id)
    #respond_to do |format|

      if @like.save
        render :json => {response: "like saved successfully"}
        #format.html { redirect_to microposts_path, notice: 'You have liked the post.' }
        #format.json { render :show, status: :created, location: @like }
      else
        render :json => {response: "like is not saved successfully"}
        #format.html { render :new }
        #format.json { render json: @like.errors, status: :unprocessable_entity }
      #end
    end
  end

  # PATCH/PUT /likes/1
  # PATCH/PUT /likes/1.json
  def update
    respond_to do |format|
      if @like.update(like_params)
        format.html { redirect_to @like, notice: 'Like was successfully updated.' }
        format.json { render :show, status: :ok, location: @like }
      else
        format.html { render :edit }
        format.json { render json: @like.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /likes/1
  # DELETE /likes/1.json
  def destroy
    #require 'byebug'
    #debugger
    @like.destroy
    respond_to do |format|
      format.html { redirect_to microposts_path, notice: 'Like was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_like
      @like = Like.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def like_params
      #debugger
      params.require(:like).permit(:user_id, :micropost_id)
    end
end
